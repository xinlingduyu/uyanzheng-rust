//! # Nakamasa-Ichika 应用程序入口
//!
//! Nakamasa-Ichika 是一个高性能的用户认证和应用管理后端服务。
//!
//! ## 功能特性
//!
//! - **用户认证**: 支持账号密码、手机号、邮箱、卡密等多种登录方式
//! - **多应用支持**: 单实例支持多个应用，独立配置和隔离
//! - **OAuth2.0**: 支持 QQ、微信等第三方登录
//! - **代理系统**: 完善的代理推广和分成机制
//! - **积分系统**: 灵活的积分规则和交易管理
//! - **支付集成**: 支持支付宝、微信等主流支付方式
//!
//! ## 架构设计
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │                        main.rs                                  │
//! │  ┌─────────────┐  ┌─────────────┐  ┌─────────────────────────┐ │
//! │  │   config    │  │    core     │  │          app            │ │
//! │  │  配置管理    │  │  核心基础    │  │      业务逻辑层         │ │
//! │  └─────────────┘  └─────────────┘  └─────────────────────────┘ │
//! └─────────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 快速开始
//!
//! 1. 首次运行：访问 `/admin/install` 完成安装
//! 2. 管理后台：访问 `/admin` 进行配置管理
//! 3. API 文档：参考各模块的文档注释
//!
//! ## 环境要求
//!
//! - Rust 1.85+
//! - MySQL 5.7+ / 8.0+
//! - Redis 6.0+
//!
//! ## 配置文件
//!
//! 配置文件位于 `config.yaml`，包含数据库、Redis、服务器等配置。

