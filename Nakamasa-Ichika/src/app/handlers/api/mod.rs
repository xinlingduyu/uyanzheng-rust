//! # API 处理器模块 (API Handlers Module)
//!
//! 本模块包含所有业务 API 的处理逻辑和路由定义。
//!
//! ## 架构概览
//!
//! ```text
//! api/
//! ├── mod.rs              # 模块入口和路由导出
//! ├── admin.rs            # 管理员 API 路由定义
//! ├── admin/              # 管理员 API 处理器
//! │   ├── app.rs          # 应用管理
//! │   ├── user.rs         # 用户管理
//! │   ├── agent.rs        # 代理管理
//! │   ├── cdk.rs          # 卡密管理
//! │   ├── fen.rs          # 积分管理
//! │   ├── goods.rs        # 商品管理
//! │   ├── order.rs        # 订单管理
//! │   ├── notice.rs       # 公告管理
//! │   ├── log.rs          # 日志管理
//! │   └── index.rs        # 管理后台首页
//! ├── user.rs             # 用户 API 路由定义
//! ├── user/               # 用户 API 处理器
//! │   ├── sign_in.rs       # 登录
//! │   ├── signUp.rs       # 注册
//! │   ├── userInfo.rs     # 用户信息
//! │   ├── fen.rs          # 积分操作
//! │   ├── cdk.rs          # 卡密使用
//! │   ├── diary.rs        # 签到
//! │   └── ...
//! └── index/              # 首页 API 处理器
//!     ├── appinfo.rs      # 应用信息
//!     ├── config.rs       # 配置获取
//!     └── install.rs      # 安装向导
//! ```
//!
//! ## API 路径规范
//!
//! | 路径前缀 | 类型 | 认证要求 |
//! |----------|------|----------|
//! | `/api/admin/*` | 管理员 API | AdminAuth |
//! | `/api/user/*` | 用户 API | UserAuth |
//! | `/api/index/*` | 公开 API | 可选 |
//! | `/api/install/*` | 安装 API | 无 |

// ============================================================================
// 子模块声明
// ============================================================================

/// 管理员 API 处理器
///
/// 包含管理后台的所有 API 实现。
pub mod admin;

/// 用户 API 处理器
///
/// 包含用户端的所有 API 实现。
pub mod user;

/// 首页/公开 API 处理器
///
/// 包含公开访问的 API 实现，如应用信息、安装向导等。
pub mod index;

// ============================================================================
// 路由导出
// ============================================================================

pub use admin::admin_routes;
pub use index::{index_routes, install_routes};
pub use user::routes as user_routes;
