//! # 请求处理器模块 (Handlers Module)
//!
//! 本模块包含所有 HTTP 请求的处理逻辑，按功能分组组织。
//!
//! ## 架构概览
//!
//! ```text
//! handlers/
//! ├── mod.rs              # 模块入口
//! ├── health.rs           # 健康检查处理器
//! ├── hello.rs            # 欢迎页处理器
//! ├── static_files.rs     # 静态文件处理器
//! └── api/                # API 处理器
//!     ├── mod.rs          # API 入口和路由导出
//!     ├── admin.rs        # 管理员 API 路由定义
//!     ├── admin/          # 管理员 API 处理器
//!     │   ├── app.rs      # 应用管理
//!     │   ├── user.rs     # 用户管理
//!     │   └── ...
//!     ├── user.rs         # 用户 API 路由定义
//!     ├── user/           # 用户 API 处理器
//!     │   ├── signIn.rs   # 登录
//!     │   ├── signUp.rs   # 注册
//!     │   └── ...
//!     └── index/          # 首页 API 处理器
//! ```
//!
//! ## 处理器规范
//!
//! ### 命名规范
//!
//! - 文件名：使用 snake_case，与功能相关
//! - 处理器函数：使用 `handle_` 前缀或直接使用功能名
//! - 路由函数：使用 `_routes` 后缀
//!
//! ### 响应规范
//!
//! 所有 API 响应使用 `ApiResponse<T>` 包装：
//!
//! ```rust,ignore
//! // 成功响应
//! ApiResponse::success(data, "操作成功")
//!
//! // 错误响应
//! ApiResponse::error("错误信息", error_code)
//! ```
//!
//! ### 错误码规范
//!
//! | 范围 | 类型 |
//! |------|------|
//! | 100-199 | 参数错误 |
//! | 200-299 | 认证错误 |
//! | 300-399 | 权限错误 |
//! | 400-499 | 业务错误 |
//! | 500-599 | 服务器错误 |

