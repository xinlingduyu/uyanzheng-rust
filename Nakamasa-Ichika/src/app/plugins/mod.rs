//! # 插件模块 (Plugins Module)
//!
//! 本模块提供可热插拔的功能扩展，支持加密、支付、通知等功能。
//!
//! ## 架构概览
//!
//! ```text
//! plugins/
//! ├── mod.rs              # 模块入口
//! ├── encryption/         # 加密插件
//! │   ├── mod.rs          # 加密接口定义
//! │   ├── aes.rs          # AES 加密实现
//! │   ├── des.rs          # DES 加密实现
//! │   └── xor.rs          # XOR 加密实现
//! ├── mailer/             # 邮件插件
//! │   ├── mod.rs          # 邮件发送接口
//! │   └── smtp.rs         # SMTP 实现
//! ├── pay/                # 支付插件
//! │   ├── mod.rs          # 支付接口定义
//! │   ├── alipay.rs       # 支付宝
//! │   └── wechat.rs       # 微信支付
//! └── sms/                # 短信插件
//!     ├── mod.rs          # 短信发送接口
//!     └── aliyun.rs       # 阿里云短信
//! ```
//!
//! ## 插件设计原则
//!
//! ### 1. 接口抽象
//!
//! 每个插件类型都定义统一的 trait 接口：
//!
//! ```rust,ignore
//! pub trait Encryption: Send + Sync {
//!     fn encode(&self, data: &str) -> Result<String, Error>;
//!     fn decode(&self, data: &str) -> Result<String, Error>;
//! }
//! ```
//!
//! ### 2. 工厂模式
//!
//! 使用工厂函数创建插件实例：
//!
//! ```rust,ignore
//! let encryptor = encryption::create_encryption(&config);
//! ```
//!
//! ### 3. 配置驱动
//!
//! 插件行为通过配置文件控制：
//!
//! ```yaml
//! plugins:
//!   encryption:
//!     type: aes
//!     key: "your-secret-key"
//!   sms:
//!     provider: aliyun
//!     access_key: "xxx"
//! ```
//!
//! ## 插件列表
//!
//! | 插件 | 功能 | 配置位置 |
//! |------|------|----------|
//! | `encryption` | 数据加密/解密 | `u_app_mi` 表 |
//! | `mailer` | 邮件发送 | 配置文件 |
//! | `pay` | 在线支付 | 配置文件 + `u_app_extend` 表 |
//! | `sms` | 短信验证码 | 配置文件 |

// ============================================================================
// 子模块声明
// ============================================================================

/// 加密插件
///
/// 提供数据加密和解密功能，支持 AES、DES、XOR 等算法。
/// 用于 API 请求数据的安全传输。
///
/// # 支持的加密类型
///
/// - `aes`: AES-128-CBC 加密
/// - `des`: DES-CBC 加密
/// - `xor`: 简单异或加密
///
/// # 配置示例
///
/// ```json
/// {
///     "enc_type": "aes",
///     "key": "1234567890123456",
///     "iv": "1234567890123456"
/// }
/// ```
pub mod encryption;

/// 邮件插件
///
/// 提供邮件发送功能，用于验证码、通知等场景。
///
/// # 功能
///
/// - 发送验证码邮件
/// - 发送通知邮件
/// - HTML 邮件支持
///
/// # 配置
///
/// 在 `config.yaml` 中配置 SMTP 服务器信息。
pub mod mailer;

/// 支付插件
///
/// 提供在线支付功能，支持支付宝、微信支付等。
///
/// # 支持的支付方式
///
/// - 支付宝 (alipay)
/// - 微信支付 (wechat)
///
/// # 流程
///
/// 1. 创建订单
/// 2. 生成支付参数
/// 3. 接收支付回调
/// 4. 更新订单状态
pub mod pay;

/// 短信插件
///
/// 提供短信发送功能，用于验证码、通知等场景。
///
/// # 支持的服务商
///
/// - 阿里云短信
///
/// # 功能
///
/// - 发送验证码短信
/// - 发送通知短信
/// - 模板消息支持
pub mod sms;

// ============================================================================
// 公开导出
// ============================================================================

pub use encryption::{Encryption, EncryptionConfig, create_encryption};
