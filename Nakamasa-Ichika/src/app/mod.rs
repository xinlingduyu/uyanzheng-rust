//! # 应用层模块 (Application Layer Module)
//!
//! 本模块包含应用的业务逻辑层，负责处理 HTTP 请求、响应生成、
//! 中间件处理和插件集成。
//!
//! ## 架构概览
//!
//! ```text
//! app/
//! ├── mod.rs              # 模块入口和导出
//! ├── routes.rs           # 路由定义
//! │
//! ├── handlers/           # 请求处理器
//! │   ├── mod.rs
//! │   ├── health.rs       # 健康检查
//! │   ├── hello.rs        # 欢迎页面
//! │   ├── static_files.rs # 静态文件服务
//! │   └── api/            # API 处理器
//! │       ├── mod.rs
//! │       ├── admin.rs    # 管理员 API
//! │       ├── admin/      # 管理员子模块
//! │       ├── user.rs     # 用户 API
//! │       ├── user/       # 用户子模块
//! │       └── index/      # 首页 API
//! │
//! ├── models/             # 数据模型
//! │   ├── mod.rs
//! │   ├── enums.rs        # 枚举类型
//! │   ├── common.rs       # 通用结构体
//! │   ├── requests.rs     # 请求 DTO
//! │   ├── responses.rs    # 响应 DTO
//! │   └── ...             # 数据库实体
//! │
//! ├── middleware/         # 中间件
//! │   ├── mod.rs
//! │   ├── admin_auth.rs   # 管理员认证
//! │   ├── user_auth.rs    # 用户认证
//! │   ├── app_context.rs  # 应用上下文
//! │   ├── cors.rs         # CORS 处理
//! │   └── body_reader.rs  # 请求体读取
//! │
//! ├── utils/              # 工具函数
//! │   ├── mod.rs
//! │   ├── response.rs     # 响应构建器
//! │   └── validator.rs    # 参数验证
//! │
//! └── plugins/            # 插件系统
//!     ├── mod.rs
//!     ├── encryption/     # 加密插件
//!     ├── mailer/         # 邮件插件
//!     ├── pay/            # 支付插件
//!     └── sms/            # 短信插件
//! ```
//!
//! ## 分层架构
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────┐
//! │                     HTTP Request                            │
//! └─────────────────────────┬───────────────────────────────────┘
//!                           │
//!                           ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                   Middleware Layer                          │
//! │  ┌─────────┐ ┌─────────┐ ┌─────────┐ ┌─────────────────┐   │
//! │  │  CORS   │ │I18n     │ │AppContext│ │AdminAuth/UserAuth│  │
//! │  └─────────┘ └─────────┘ └─────────┘ └─────────────────┘   │
//! └─────────────────────────┬───────────────────────────────────┘
//!                           │
//!                           ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                   Handler Layer                             │
//! │  ┌─────────────────┐ ┌─────────────────┐ ┌─────────────┐   │
//! │  │ Admin Handlers  │ │  User Handlers  │ │Index Handlers│  │
//! │  └─────────────────┘ └─────────────────┘ └─────────────┘   │
//! └─────────────────────────┬───────────────────────────────────┘
//!                           │
//!                           ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                   Service/Model Layer                       │
//! │  ┌─────────────────┐ ┌─────────────────┐ ┌─────────────┐   │
//! │  │    Models       │ │    Plugins      │ │    Utils    │   │
//! │  └─────────────────┘ └─────────────────┘ └─────────────┘   │
//! └─────────────────────────────────────────────────────────────┘
//! ```
//!
//! ## 模块职责
//!
//! | 模块 | 职责 | 依赖 |
//! |------|------|------|
//! | `handlers` | 处理 HTTP 请求，调用业务逻辑 | models, utils, core |
//! | `models` | 数据结构定义，数据库映射 | core, serde |
//! | `middleware` | 请求预处理和后处理 | core, models |
//! | `utils` | 通用工具函数 | core |
//! | `plugins` | 可插拔功能模块 | core, models |

// ============================================================================
// 子模块声明
// ============================================================================

/// 请求处理器模块
///
/// 包含所有 HTTP 请求的处理逻辑，按 API 类型分组。
pub mod handlers;

/// 数据模型模块
///
/// 定义数据库实体和 API 数据传输对象。
pub mod models;

/// 工具函数模块
///
/// 提供响应构建、参数验证等通用功能。
pub mod utils;

/// 中间件模块
///
/// 提供认证、授权、CORS 等中间件。
pub mod middleware;

/// 插件模块
///
/// 支持热插拔的功能扩展，如加密、支付、通知等。
pub mod plugins;

/// 路由定义模块
///
/// 定义应用的 URL 路由规则和路由分组。
pub mod routes;

