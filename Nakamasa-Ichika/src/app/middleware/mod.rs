/// 管理员认证中间件
///
/// 验证管理员的 Token 和权限，支持密码 MD5 验证。
/// 从 Depot 获取管理员 ID 和密码 MD5，验证成功后注入管理员信息。
///
/// # 注入数据
///
/// - `admin_id`: 管理员 ID
/// - `admin_info`: 管理员详细信息
///
/// # 错误码
///
/// - 101: Token 不能为空
/// - 102: Token 无效
/// - 103: 账号已被禁用
pub mod admin_auth;

/// 用户认证中间件
///
/// 验证用户的 Token 和设备绑定，支持加密数据解密。
/// 完整实现 PHP 版本的 `__init`, `__dataCheck`, `__TokenCheck` 逻辑。
///
/// # 注入数据
///
/// - `user_uid`: 用户 ID
/// - `user_udid`: 设备 ID
/// - `user_appid`: 应用 ID
/// - `user_info`: 用户详细信息
/// - `post_params`: POST 参数
/// - `client_ip`: 客户端 IP
///
/// # 错误码
///
/// - 103: 登录功能已关闭
/// - 109: 签名验证失败
/// - 110: 请求已过期
/// - 111: 数据不能为空
/// - 113: 数据解密失败
/// - 127: 账号已被禁用
/// - 128: Token 无效
/// - 129: 用户不存在
/// - 130: 设备不匹配
/// - 131: Token 已过期
pub mod user_auth;

/// 应用上下文中间件
///
/// 解析请求中的 appid 参数，加载应用配置信息。
/// 作为 UserAuth 的前置中间件。
///
/// # 注入数据
///
/// - `app_appid`: 应用 ID
/// - `app_info`: 应用配置信息
///
/// # 参数来源
///
/// 优先级：Header > Query > POST
pub mod app_context;

/// CORS 跨域处理中间件
///
/// 处理跨域请求，添加必要的 CORS 响应头。
/// 支持 preflight OPTIONS 请求。
///
/// # 响应头
///
/// - `Access-Control-Allow-Origin`: 允许的源
/// - `Access-Control-Allow-Methods`: 允许的方法
/// - `Access-Control-Allow-Headers`: 允许的头
/// - `Access-Control-Allow-Credentials`: 允许凭证
pub mod cors;

/// 请求体读取中间件（当前未使用，保留以供参考）
pub mod connect;

