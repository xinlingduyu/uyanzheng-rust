//! # 中间件模块 (Middleware Module)
//!
//! 本模块包含所有 HTTP 请求的中间件实现，用于请求预处理和后处理。
//!
//! ## 架构概览
//!
//! ```text
//! middleware/
//! ├── mod.rs              # 模块入口
//! ├── admin_auth.rs       # 管理员认证中间件
//! ├── user_auth.rs        # 用户认证中间件
//! ├── app_context.rs      # 应用上下文中间件
//! ├── cors.rs             # CORS 跨域处理中间件
//! └── body_reader.rs      # 请求体读取中间件
//! ```
//!
//! ## 中间件执行顺序
//!
//! ```text
//! Request
//!    │
//!    ▼
//! ┌─────────────────┐
//! │     CORS        │  ← 处理跨域请求
//! └────────┬────────┘
//!          │
//!          ▼
//! ┌─────────────────┐
//! │   I18nMiddleware │  ← 国际化处理 (core 模块)
//! └────────┬────────┘
//!          │
//!          ▼
//! ┌─────────────────┐
//! │   AppContext    │  ← 应用上下文注入
//! └────────┬────────┘
//!          │
//!          ▼
//! ┌─────────────────┐
//! │  UserAuth /     │  ← 用户/管理员认证
//! │  AdminAuth      │
//! └────────┬────────┘
//!          │
//!          ▼
//!      Handler
//!          │
//!          ▼
//!     Response
//! ```
//!
//! ## 中间件职责
//!
//! | 中间件 | 职责 | 注入数据 |
//! |--------|------|----------|
//! | `cors` | 处理跨域请求头 | - |
//! | `AppContext` | 解析 appid，加载应用配置 | `app_appid`, `app_info` |
//! | `UserAuth` | 验证用户 Token，加载用户信息 | `user_uid`, `user_info` |
//! | `AdminAuth` | 验证管理员 Token，加载权限 | `admin_id`, `admin_info` |

