//! # 工具函数模块 (Utilities Module)
//!
//! 本模块提供通用的工具函数，供其他模块使用。
//!
//! ## 架构概览
//!
//! ```text
//! utils/
//! ├── mod.rs              # 模块入口
//! ├── response.rs         # API 响应构建器
//! └── validator.rs        # 参数验证工具
//! ```
//!
//! ## 模块职责
//!
//! | 模块 | 职责 |
//! |------|------|
//! | `response` | 统一的 API 响应格式 |
//! | `validator` | 请求参数验证 |

// ============================================================================
// 子模块声明
// ============================================================================

/// API 响应构建器
///
/// 提供统一的 API 响应格式，支持成功和错误响应。
///
/// # 响应格式
///
/// ```json
/// {
///     "code": 0,
///     "msg": "操作成功",
///     "data": { ... }
/// }
/// ```
///
/// # 使用示例
///
/// ```rust,ignore
/// use crate::app::utils::response::ApiResponse;
///
/// // 成功响应
/// let response = ApiResponse::success(user_info, "获取成功");
///
/// // 错误响应
/// let response = ApiResponse::<()>::error("参数错误", 101);
/// ```
pub mod response;

/// 参数验证工具
///
/// 提供常用的参数验证函数，如手机号、邮箱、密码等。
///
/// # 支持的验证类型
///
/// - 手机号格式验证
/// - 邮箱格式验证
/// - 密码强度验证
/// - 用户名格式验证
///
/// # 使用示例
///
/// ```rust,ignore
/// use crate::app::utils::validator::{validate_phone, validate_email};
///
/// if !validate_phone(&phone) {
///     return ApiResponse::<()>::error("手机号格式不正确", 101);
/// }
/// ```
pub mod validator;

// ============================================================================
// 公开导出
// ============================================================================

pub use response::ApiResponse;
pub use validator::*;
